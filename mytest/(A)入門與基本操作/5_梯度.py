import torch

# 創建一個張量並啟用梯度計算
x = torch.tensor([2.0, 3.0], requires_grad=True)

# 定義一個簡單的函數 y = x1^2 + x2^3
y = x[0]**2 + x[1]**3

'''
(計算 y 對 x 的梯度)計算梯度的過程如下
1. 對 x[0] 的偏導數： 
   ∂y/∂x[0] = ∂(x[0]^2 + x[1]^3)/∂x[0] = 2 * x[0] 
   當 x[0] = 2.0 時，∂y/∂x[0] = 2 * 2.0 = 4.0
2. 對 x[1] 的偏導數： 
   ∂y/∂x[1] = ∂(x[0]^2 + x[1]^3)/∂x[1] = 3 * x[1]^2 
   當 x[1] = 3.0 時，∂y/∂x[1] = 3 * (3.0)^2 = 3 * 9.0 = 27.0
因此，y 對 x 的梯度為 [4.0, 27.0]，這個結果存儲在 x.grad 中
(偏導數的作用是用來衡量一個函數在某一變數上的變化率)
'''
y.backward()

# 輸出梯度 (x.grad：存儲梯度，對應於 y 對 x 的偏導數)
print("x 的梯度:", x.grad)
